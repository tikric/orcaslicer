function resolveRealImage(src: any, category: PhotoCategory): string {
  if (!src) {
    return CATEGORY_URLS[category] || CATEGORY_URLS.layer;
  }

  let pathStr = "";
  if (typeof src === "string") {
    pathStr = src;
  } else if (typeof src === "object" && src !== null) {
    if (typeof src.src === "string") {
      pathStr = src.src;
    } else if (typeof src.default === "string") {
      pathStr = src.default;
    } else if (src.default && typeof src.default === "object" && typeof src.default.src === "string") {
      pathStr = src.default.src;
    } else if (typeof src.url === "string") {
      pathStr = src.url;
    } else {
      // Fallback: search for any string key
      for (const key of ["src", "default", "url"]) {
        if (typeof src[key] === "string") {
          pathStr = src[key];
          break;
        }
      }
    }
  }

  if (typeof pathStr === "string" && pathStr) {
    // 1. If it contains "guides/", it is a development path like "/src/assets/guides/qual-layer-height.jpg"
    if (pathStr.includes("guides/")) {
      const parts = pathStr.split("guides/");
      const fileName = parts[parts.length - 1].split("?")[0];
      return `/guides/${fileName}`;
    }

    // 2. If it is in production and hashed like "/assets/qual-layer-height-B8y3D_u9.jpg"
    const lastSlash = pathStr.lastIndexOf('/');
    const fileNameWithQuery = lastSlash !== -1 ? pathStr.substring(lastSlash + 1) : pathStr;
    const fileName = fileNameWithQuery.split("?")[0];

    const knownPrefixes = /^(qual|vel|sup|res|real|wall|xy|seam|scarf|staggered)-/;
    if (knownPrefixes.test(fileName)) {
      // Strip the 8-character Vite hash (e.g., -B8y3D_u9.jpg or .B8y3D_u9.jpg)
      const hashRegex = /[-.][a-zA-Z0-9_~]{8}\.jpg$/i;
      if (hashRegex.test(fileName)) {
        const originalName = fileName.replace(hashRegex, '.jpg');
        return `/guides/${originalName}`;
      }
      return `/guides/${fileName}`;
    }

    return pathStr;
  }

  return CATEGORY_URLS[category] || CATEGORY_URLS.layer;
}

export const GUIDE_IMAGES: Record<string, GuideImage> = {};

// Build GUIDE_IMAGES dynamically with resolved high-quality images
Object.entries(exactCategoryById).forEach(([id, category]) => {
  GUIDE_IMAGES[id] = {
    src: resolveRealImage(PHOTO_LIBRARY[category], category),
    caption: captions[category],
  };
});

// Apply direct overrides, ensuring any placeholder images are resolved to high-quality fallbacks
Object.entries(directOverrides).forEach(([id, guideImage]) => {
  const category = getCategoryForItem(id);
  GUIDE_IMAGES[id] = {
    src: resolveRealImage(guideImage.src, category),
    caption: guideImage.caption,
  };
});

function getCategoryForItem(itemId: string, label = ""): PhotoCategory {
  if (itemId in exactCategoryById) return exactCategoryById[itemId];

  const key = `${itemId} ${label}`.toLowerCase();

  if (
    key.includes("multi") ||
    key.includes("filament") ||
    key.includes("purge") ||
    key.includes("prime") ||
    key.includes("tower") ||
    key.includes("torre")
  )
    return "multimaterial";
  if (key.includes("fuzzy") || key.includes("textura difusa")) return "fuzzy";
  if (key.includes("raft") || key.includes("jangada")) return "raft";
  if (key.includes("support") || key.includes("suporte") || key.includes("interface"))
    return "support";
  if (
    key.includes("speed") ||
    key.includes("veloc") ||
    key.includes("accel") ||
    key.includes("acel") ||
    key.includes("jerk") ||
    key.includes("travel") ||
    key.includes("desloc")
  )
    return "speed";
  if (
    key.includes("bridge") ||
    key.includes("ponte") ||
    key.includes("overhang") ||
    key.includes("sali")
  )
    return "bridge";
  if (key.includes("iron") || key.includes("alis")) return "ironing";
  if (
    key.includes("seam") ||
    key.includes("costura") ||
    key.includes("wipe") ||
    key.includes("limpeza")
  )
    return "seam";
  if (
    key.includes("infill") ||
    key.includes("preench") ||
    key.includes("solid") ||
    key.includes("density") ||
    key.includes("densidade")
  )
    return "infill";
  if (
    key.includes("wall") ||
    key.includes("parede") ||
    key.includes("shell") ||
    key.includes("casca") ||
    key.includes("surface") ||
    key.includes("superf")
  )
    return "wall";
  if (
    key.includes("line-width") ||
    key.includes("linha") ||
    key.includes("flow") ||
    key.includes("fluxo")
  )
    return "lineWidth";
  if (
    key.includes("first-layer") ||
    key.includes("primeira") ||
    key.includes("brim") ||
    key.includes("skirt") ||
    key.includes("saia") ||
    key.includes("borda") ||
    key.includes("elephant")
  )
    return "adhesion";

  return "layer";
}

export function getGuideImageForItem(itemId: string, label?: string, value?: string): GuideImage {
  let exact = GUIDE_IMAGES[itemId];
  const category = getCategoryForItem(itemId, label);
  const fallbackUrl = CATEGORY_URLS[category] || CATEGORY_URLS.layer;

  if (!exact) {
    // Try suffix matches (-sup for Support, -mm for Multimaterial)
    if (GUIDE_IMAGES[`${itemId}-sup`]) {
      exact = GUIDE_IMAGES[`${itemId}-sup`];
    } else if (GUIDE_IMAGES[`${itemId}-mm`]) {
      exact = GUIDE_IMAGES[`${itemId}-mm`];
    }
  }

  // Explicit mappings for parameters whose IDs mismatch between directOverrides and simulator
  if (!exact) {
    const specialMappings: Record<string, string> = {
      "support-z-distance": "top-z-distance-sup",
      "support-interface-material": "support-raft-interface-filament-sup",
      "prime-tower-enable": "enable-prime-tower-mm",
      "prime-tower-width": "prime-tower-width-mm",
      "flush-into-infill": "purge-into-infill-mm",
      "ooze-prevention-temp": "enable-ooze-prevention-mm",
      "flushing-multiplier": "prime-volume-mm",
      "flush-into-support": "purge-into-supports-mm",
    };

    const targetKey = specialMappings[itemId];
    if (targetKey && GUIDE_IMAGES[targetKey]) {
      exact = GUIDE_IMAGES[targetKey];
    }
  }

  if (exact) {
    return {
      ...exact,
      fallbackSrc: fallbackUrl,
    };
  }

  // Use local high-quality category-level visual guides as primary when no exact override is present.
  // This completely avoids iframe CORS/CSP blocking and 404 latency of requesting non-existent remote images.
  const valueSuffix = value ? ` Valor mostrado: ${value}.` : "";
  const baseCategorySrc = resolveRealImage(PHOTO_LIBRARY[category], category);

  return {
    src: baseCategorySrc,
    caption: `${captions[category]}${valueSuffix}`,
    fallbackSrc: fallbackUrl,
  };
}
